package com.sparebyte.models;

/**
 * This is the Product model class
 * 
 * @author Malshan Rathnayake, SLIIT Undergraduate
 * @version 1.0
 */

public class Product {
	
	private String productID;
	private String productName;
	private String productDec;
	private String productCategory;
	private String productBrand;
	private String productModel;
	private String productPrice;
	private String productStock;
	private String productImagePath;
	
	/**
	 * @return the productID
	 */
	public String getProductID() {
		return productID;
	}
	
	/**
	 * @param set the productID
	 */
	public void setProductID(String productID) {
		this.productID = productID;
	}
	
	/**
	 * @return the productName
	 */
	public String getProductName() {
		return productName;
	}
	
	/**
	 * @param set the productName
	 */
	public void setProductName(String productName) {
		this.productName = productName;
	}
	
	/**
	 * @return the productDec
	 */
	public String getProductDec() {
		return productDec;
	}
	
	/**
	 * @param set the productDec
	 */
	public void setProductDec(String productDec) {
		this.productDec = productDec;
	}
	
	/**
	 * @return the productCategory
	 */
	public String getProductCategory() {
		return productCategory;
	}
	
	/**
	 * @param set the productCategory
	 */
	public void setProductCategory(String productCategory) {
		this.productCategory = productCategory;
	}
	
	/**
	 * @return the productBrand
	 */
	public String getProductBrand() {
		return productBrand;
	}
	
	/**
	 * @param set the productBrand
	 */
	public void setProductBrand(String productBrand) {
		this.productBrand = productBrand;
	}
	
	/**
	 * @return the productModel
	 */
	public String getProductModel() {
		return productModel;
	}
	
	/**
	 * @param set the productModel
	 */
	public void setProductModel(String productModel) {
		this.productModel = productModel;
	}
	
	/**
	 * @return the productPrice
	 */
	public String getProductPrice() {
		return productPrice;
	}
	
	/**
	 * @param set the productPrice
	 */
	public void setProductPrice(String productPrice) {
		this.productPrice = productPrice;
	}
	
	/**
	 * @return the productStock
	 */
	public String getProductStock() {
		return productStock;
	}
	
	/**
	 * @param set the productStock
	 */
	public void setProductStock(String productStock) {
		this.productStock = productStock;
	}
	
	/**
	 * @return the productImagePath
	 */
	public String getProductImagePath() {
		return productImagePath;
	}
	
	/**
	 * @param set the productImagePath
	 */
	public void setProductImagePath(String productImagePath) {
		this.productImagePath = productImagePath;
	}
	
	
}
